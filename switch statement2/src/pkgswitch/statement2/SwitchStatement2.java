
package pkgswitch.statement2;


public class SwitchStatement2 {

   
    public static void main(String[] args) {
        int a=1;
        switch (a){
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            default:
                System.out.println("other number");
                                
        }
    }
    
}

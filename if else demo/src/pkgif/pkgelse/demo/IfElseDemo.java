
package pkgif.pkgelse.demo;


public class IfElseDemo {

    
    public static void main(String[] args) {
       int num = 5;
       if (num == 85){
           System.out.println("Your num is 85");
       }else if (num > 10){
           System.out.println("your num is greater than 10");
       }else{
           System.out.println("your num less than 10");
       }
    }
    
}

 package com.branay;


public class Student {
    private String name;
    private int roll_no;
    
    public void setName(String nName){
        name = nName;
    }
    
    public void setRoll_no(int nRoll_no){
        roll_no = nRoll_no;
    }
    
    public String getName(){
        return name;
    }
    
    public int getRoll_no(){
        return roll_no;
    }
    
    
}


package pkgswitch.statement.demo;


public class SwitchStatementDemo {

    
    public static void main(String[] args) {
        int a =1;
        
        switch (a){
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("Two");
                break;
            default:
                System.out.println("other numbers");
            
        }
    }
    
}


package array;


public class Array {

    
    public static void main(String[] args) {
        
        int ar[]= new int[3];
        ar[0] =4;
        ar[1] =5;
        ar[2] =6;
        
        System.out.println(ar[0]);
        System.out.println(ar[1]);
        System.out.println(ar[2]);
        
        System.out.println("***************************");
        
        for(int i=0; i<=2; i++){
            System.out.println(ar[i]);
        }
        
        
    }
      

    
}
    

